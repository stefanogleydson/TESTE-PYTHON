#chamada "Calculadora"
class Calculadora:


    #CRIAR O METODO chamado 'soma':

    def soma (self, n1 , n2):
        return n1 + n2
    
    def sub (self, n1 , n2):
        return n1 - n2
    
    def mult (self, n1 , n2):
        return n1 * n2
    
    def div (self, n1 , n2):
        return n1 / n2